    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jDateChooser1.getDateFormatString().isEmpty() || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "One or more fileds are empty! please fill all fields!!");
        } else {

            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection(
                        "jdbc:mysql://localhost:3306/registration", "root", "root");
                System.out.println("DB connected");

                String query = "INSERT INTO `registration`.`user_details` (`first_name`,`Last_name`,`address`,`dob`,`contact_number`,`user_name`,`password`) VALUES (?,?,?,?,?,?,?)";

                ps = con.prepareStatement(query);

                ps.setString(1, jTextField1.getText());
                ps.setString(2, jTextField2.getText());
                ps.setString(3, jTextField3.getText());
                // conversion between string and sql date format
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(jDateChooser1.getDate());
                ps.setString(4, date);

                //ps.setString(5, jTextField4.getText());
                ps.setInt(5, Integer.parseInt(jTextField4.getText()));
                ps.setString(6, jTextField5.getText());
                ps.setString(7, jTextField6.getText());

                ps.executeUpdate();
                con.close();
                JOptionPane.showMessageDialog(null, "Registered Successfully!!!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jTextField6FocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField6FocusGained
        // validation in check the user name exisist or not
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/registration", "root", "root");

            Statement stmt = con.createStatement();

            String userName = jTextField5.getText();

            String query = "SELECT * FROM registration.user_details where user_name = '" + userName + "'";

            ResultSet rs = stmt.executeQuery(query);

            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "USER NAME ALREADY EXISITS TRY ANOTHER!!");
               jTextField5.setText("");
               jTextField5.requestFocus();
            }
            con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }


    }//GEN-LAST:event_jTextField6FocusGained

